def get_name(pid: int, lang: str = "en") -> str:
    if lang == "fr":
        return POKEMON_NAMES_FR.get(pid, POKEMON_NAMES.get(pid, f"#{pid}"))
    return POKEMON_NAMES.get(pid, f"#{pid}")


def search_pokemon(query: str, lang: str = "en") -> list[tuple[int, str]]:
    q = query.strip().lower()
    names = POKEMON_NAMES_FR if lang == "fr" else POKEMON_NAMES
    if not q:
        return [(pid, names.get(pid, POKEMON_NAMES[pid])) for pid in ALL_IDS]
    return [
        (pid, names.get(pid, POKEMON_NAMES.get(pid, "")))
        for pid in ALL_IDS
        if q in names.get(pid, POKEMON_NAMES.get(pid, "")).lower()
        or q in POKEMON_NAMES.get(pid, "").lower()
        or q == str(pid)
    ]
